let grade = "A";

switch (grade) {
    case "A":
        console.log("Outstanding!");
        break;
    case "B":
        console.log("Very Good!");
        break;
    case "C":
        console.log("Good Effort!");
        break;
    case "D":
        console.log("Needs Improvement.");
        break;
    case "E":
        console.log("Not good.");
        break;
    case "F":
        console.log("Failed. Must Retake the Exam.");
        break;
    default:
        console.log("Invalid grade.")
}